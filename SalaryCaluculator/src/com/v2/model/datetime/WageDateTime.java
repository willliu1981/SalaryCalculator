package com.v2.model.datetime;

public class WageDateTime {
	private String name;

	public WageDateTime() {

	}

	public WageDateTime(String name) {
		this.name = name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

	public String toString() {
		return this.name;
	}
}
